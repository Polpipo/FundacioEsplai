public class Ejercicio7 {
	public static void main(String[] args) {
		//Lógicos
		System.out.println("Lógicos:");
		final boolean BOL = false;
		System.out.println(BOL + "\n");
		
		
		//Caracter
		System.out.println("Caracter:");
		final char CARACTER = 'a';
		System.out.println(CARACTER + "\n");
		
		
		//Numéricos enteros
		System.out.println("Numéricos enteros:");
		final byte ENTERO_PEQUEÑITO = 26;
		final short ENTERO_PEQUEÑO = 260;
		final int ENTERO_MEDIANO = 305630;
		final long ENTERO_LARGO = 2343892343L;
		
		System.out.println(ENTERO_PEQUEÑITO);
		System.out.println(ENTERO_PEQUEÑO);
		System.out.println(ENTERO_MEDIANO);
		System.out.println(ENTERO_LARGO + "\n");
		
		
		//Numéricos reales
		System.out.println("Numéricos reales:");
		final double REAL_DOUBLE = 260.32;
		final float REAL_FLOAT = 49034.192F;
		
		
		System.out.println(REAL_DOUBLE);
		System.out.println(REAL_FLOAT);
	}
}
