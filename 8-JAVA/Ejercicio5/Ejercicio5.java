public class Ejercicio5 {
	public static void main(String[] args) {
		//Lógicos
		System.out.println("Lógicos:");
		boolean bol = false;
		System.out.println(bol + "\n");
		
		
		//Caracter
		System.out.println("Caracter:");
		char caracter = 'a';
		System.out.println(caracter + "\n");
		
		
		//Numéricos enteros
		System.out.println("Numéricos enteros:");
		byte enteroPequeñito = 26;
		short enteroPequeño = 260;
		int enteroMediano = 305630;
		long enteroLargo = 2343892343L;
		
		System.out.println(enteroPequeñito);
		System.out.println(enteroPequeño);
		System.out.println(enteroMediano);
		System.out.println(enteroLargo + "\n");
		
		
		//Numéricos reales
		System.out.println("Numéricos reales:");
		double realDouble = 260.32;
		float realFloat = 49034.192F;
		
		
		System.out.println(realDouble);
		System.out.println(realFloat);
	}
}
