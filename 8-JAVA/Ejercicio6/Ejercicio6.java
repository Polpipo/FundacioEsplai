public class Ejercicio6 {
	public static void main(String[] args) {
		System.out.println("SISTEMA CIRCULAR BYTE:");
		byte num1 = (byte) (127 + 5);
		System.out.println(num1 + "\n");
		
		
		System.out.println("SISTEMA CIRCULAR SHORT:");
		short num2 = (short) (32767 + 25);
		System.out.println(num2 + "\n");
		
		
		System.out.println("SISTEMA CIRCULAR INT:");
		int num3 = (int) (2147483647L + 100000);
		System.out.println(num3 + "\n");
		
		
		System.out.println("SISTEMA CIRCULAR LONG:");
		long num4 = (long) (9223372036854775807L + 200000);
		System.out.println(num4 + "\n");
	}
}
