public class Ejercicio11 {
	static int numero = 0;
	static void Incrementar() {
	    numero++;
	    System.out.println(numero);
	  }
	
	static void Decrementar() {
		numero--;
		numero--;
	    System.out.println(numero);
	  }
	
	public static void main(String[] args) {
		
		Incrementar();
		Decrementar();
		
	}
	
	
}
